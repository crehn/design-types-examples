package t_r.robust;

public class PersonalAttribute {

	private float lifeExpectancyDifferential;
	private String name;
	private String description;
	
	public float getLifeExpectancyDifferential() {
		return this.lifeExpectancyDifferential;
	}
	
	public String getName() {
		return this.name;
	}
	
	public String getDescription() {
		return this.description;
	}
}
